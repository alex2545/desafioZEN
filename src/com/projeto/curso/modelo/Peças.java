package com.projeto.curso.modelo;

public class Peças {
	
	private String nome;
	private String veiculoDeAplicaçao;
	private double pesoLiquido;
	private double pesoBruto;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getVeiculoDeAplicaçao() {
		return veiculoDeAplicaçao;
	}
	public void setVeiculoDeAplicaçao(String veiculoDeAplicaçao) {
		this.veiculoDeAplicaçao = veiculoDeAplicaçao;
	}
	public double getPesoLiquido() {
		return pesoLiquido;
	}
	public void setPesoLiquido(double pesoLiquido) {
		this.pesoLiquido = pesoLiquido;
	}
	public double getPesoBruto() {
		return pesoBruto;
	}
	public void setPesoBruto(double pesoBruto) {
		this.pesoBruto = pesoBruto;
	}
	
	

}
